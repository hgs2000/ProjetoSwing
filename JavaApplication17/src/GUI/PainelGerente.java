    private void newFilmeBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newFilmeBtnSaveActionPerformed
        //Validação dos campos informados

        if (newFilmeNome.getText().isEmpty()) {
            newFilmeNome.requestFocus();
            Msgs.displayErrorJOP("Erro! O nome do filme não pode ser vazio.", this);
            return;
        }
        if (newFilmeSinopse.getText().isEmpty()) {
            newFilmeSinopse.requestFocus();
            Msgs.displayErrorJOP("Erro! A sinopse não pode ser vazia.", this);
            return;
        }
        if (newFilmeGeneros.getText().isEmpty()) {
            newFilmeGeneros.requestFocus();
            Msgs.displayErrorJOP("Erro! O filme requer ao menos um gênero.", this);
            return;
        }
        Filme f = new Filme(newFilmeNome.getText(), newFilmeSinopse.getText(), newFilmeGeneros.getText(), newFilmeAno.getValue());
        listaFilmes.add(f);
        f.sendToBD();
        System.out.println(f.getFilmeNome());
        System.out.println(f.getFilmeSinopse());
        System.out.println(f.getFilmeAno());
        System.out.println(f.getFilmeGeneros());
        Msgs.displaySucessJOP("O filme foi registrado com sucesso!", this);
        newFilmeAno.setValue(2000);
        newFilmeGeneros.setText("");
        newFilmeNome.setText("");
        newFilmeSinopse.setText("");
    }//GEN-LAST:event_newFilmeBtnSaveActionPerformed

