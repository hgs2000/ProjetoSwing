    private void loadVendedorHashMap() {
        vends = new HashMap<>();
        int i = 0;
        for (Vendedor vend : listaVendedores) {
            vends.put(vend.getCpf(), i);
            i++;
        }
    }

    private void loadFilmeHashMap() {
        films = new HashMap<>();
        int i = 0;
        for (Filme vend : listaFilmes) {
            vends.put(vend.getFilmeNome() + " -- " + vend.getFilmeAno(), i);
            i++;
        }
    }

