    private void newFilmeBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newFilmeBtnSaveActionPerformed
        //Validação dos campos informados

        if (newFilmeNome.getText().isEmpty()) {
            newFilmeNome.requestFocus();
            return;
        }
        if (newFilmeSinopse.getText().isEmpty()) {
            newFilmeSinopse.requestFocus();
        }
        ArrayList<JCheckBox> boxes = new ArrayList<JCheckBox>() {
            {
                add(newFilmeGenAcao);
                add(newFilmeGenAnim);
                add(newFilmeGenAvent);
                add(newFilmeGenComed);
                add(newFilmeGenDrama);
                add(newFilmeGenFant);
                add(newFilmeGenFicCi);
                add(newFilmeGenGuerra);
                add(newFilmeGenHist);
                add(newFilmeGenMusi);
                add(newFilmeGenRoman);
                add(newFilmeGenTerror);
            }
        };
        ArrayList<JCheckBox> toFilmes;
        toFilmes = new ArrayList<>();
        for (JCheckBox boxe : boxes) {
            if (boxe.isSelected()) {
                toFilmes.add(boxe);
            }
        }
        listaFilmes.add(new Filme(newFilmeNome, newFilmeSinopse, toFilmes, newFilmeAno.getValue()));
    }//GEN-LAST:event_newFilmeBtnSaveActionPerformed

